import math

def golden_ratio():
    """This is the function that prints the gold relation"""
    print((1 + math.sqrt(5))/2)

def six_squared():
    """This is the function that print the square of six"""
    print(6**2)

def hypotenuse():
    """This is the function that prints length of the hypotenuse
    in square triangle with sides of length 5 and 12"""
    print(math.sqrt(5**2 + 12**2))

def pi():
    """This is the function that prints the value of pi number"""
    print(math.pi)

def e():
    """This is the function that prints value of number `e`"""
    print(math.e)

def squares_area():
    """This is the function that prints the area of squares
    with sidelengths from 1 to 10"""
    print(1**2, 2**2, 3**2, 4**2, 5**2, 6**2, 7**2, 8**2, 9**2, 10**2,)

golden_ratio()
six_squared()
hypotenuse()
pi()
e()
squares_area()